using System;

class FloatComparisonExample
{
    public void CompareFloats()
    {
        float a = 0.1f + 0.2f;
        float b = 0.3f;

        if (a == b)
        {
            Console.WriteLine("Equal");
        }
        else
        {
            Console.WriteLine("Not Equal");
        }
    }
}
